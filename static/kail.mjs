/**
 * Make a box for a message. Returns the actual message content part.
 * @param conv  Conversation the message belongs to
 * @param msg  Message to box
 * @param opts  Options (mainly about what else to include)
 */
function mkMsgBox(conv, msg, opts = {}) {
    const box = opts.box || dce("div");
    let meta = entityMeta[msg.role] || entityMeta.assistant;
    // Try to find what tool this is by looking earlier
    let suffix = "";
    if (msg.role === "tool") {
        msgLoop: for (const msg2 of conv.messages) {
            if (msg2.role !== "assistant" || !msg2.tool_calls)
                continue;
            for (const tc of msg2.tool_calls) {
                if (tc.id === msg.tool_call_id) {
                    suffix = `: ${tc.function.name}`;
                    break msgLoop;
                }
            }
        }
    }
    box.className = `msg-row entity-${msg.role}`;
    box.innerHTML = `
        <div class="msg-avatar">${meta.icon}</div>
        <div class="msg-content">
            <div class="msg-meta">
                <span class="msg-sender">${meta.label}${suffix}</span>
                <div class="msg-actions"></div>
            </div>
            <div class="msg-body"></div>
        </div>`;
    const body = box.children[1].children[1];
    const action = box.children[1].children[0].children[1];
    const actionBtns = {
        dlText: null,
        dlJSON: null,
        editText: null,
        editJSON: null,
        trunc: null,
        del: null
    };
    const ret = {
        box,
        body,
        action,
        actionBtns
    };
    // Fill in the action buttons
    function actionBtn(svg, txt) {
        const ret = dce("button");
        ret.className = "msg-action-btn";
        ret.innerHTML = `${svg}<span>${txt}</span>`;
        action.appendChild(ret);
        return ret;
    }
    function actionSep() {
        const div = dce("div");
        div.className = "msg-actions-sep";
        action.appendChild(div);
    }
    if (opts.actions !== false) {
        const detail = { conv, msg, box: ret };
        if (opts.text !== false) {
            actionBtns.dlText = actionBtn(download, "dl");
            actionBtns.dlText.onclick = () => dispatch("click-msg-dl", detail);
        }
        actionBtns.dlJSON = actionBtn((opts.text !== false) ? code : download, (opts.text !== false) ? "JSON" : "dl");
        actionBtns.dlJSON.onclick = () => dispatch("click-msg-dl-json", detail);
        actionSep();
        if (opts.text !== false) {
            actionBtns.editText = actionBtn(edit, "edit");
            actionBtns.editText.onclick = () => dispatch("click-msg-edit", detail);
        }
        actionBtns.editJSON = actionBtn((opts.text !== false) ? code : edit, (opts.text !== false) ? "JSON" : "edit");
        actionBtns.editJSON.onclick = () => dispatch("click-msg-edit-json", detail);
        actionSep();
        actionBtns.trunc = actionBtn(trunc, "trunc");
        actionBtns.trunc.onclick = () => dispatch("click-msg-trunc", detail);
        actionBtns.del = actionBtn(del, "del");
        actionBtns.del.onclick = () => dispatch("click-msg-del", detail);
    }
    if (msg.reasoning_content) {
        const rbox = mkCollapsible("reasoning");
        markdown(rbox.body, msg.reasoning_content);
        body.appendChild(rbox.box);
    }
    const content = (typeof msg.content === "string")
        ? [{ type: "text", text: msg.content }]
        : msg.content;
    for (const part of content) {
        switch (part.type) {
            case "text":
                if (msg.role === "tool") {
                    const tbox = mkCollapsible("tool");
                    body.appendChild(tbox.box);
                    tbox.body.innerText = part.text;
                }
                else {
                    const p = dce("p");
                    markdown(p, part.text);
                    body.appendChild(p);
                }
                break;
            case "image_url":
                {
                    const img = dce("img");
                    img.className = "msg-image";
                    img.src = part.image_url.url;
                    img.alt = "image";
                    body.appendChild(img);
                    img.onclick = () => openLightbox(part.image_url.url);
                    break;
                }
            case "input_audio":
                {
                    const audio = dce("audio");
                    audio.controls = true;
                    audio.src = part.input_audio.url;
                    body.appendChild(audio);
                    break;
                }
            case "input_video":
                {
                    const video = dce("video");
                    video.controls = true;
                    video.src = part.input_video.url;
                    body.appendChild(video);
                    break;
                }
        }
    }
    if (msg.tool_calls) {
        for (const tc of msg.tool_calls) {
            const tbox = mkCollapsible("tool-use", `: ${tc.function.name}`);
            tbox.body.innerText = tc.function.arguments;
            body.appendChild(tbox.box);
        }
    }
    if (!opts.box) {
        messages.appendChild(box);
        messages.scrollTop = messages.scrollHeight;
    }
    return ret;
}
